    public void getData() {
    // Mengahpus isi table datapetugas
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
    try{
        // membuat statement pemanggilan data pada table
            Statement stat = (Statement) dbkoneksi.getKoneksi().createStatement();
                    String sql= "Select * from tblpetugas";
                    ResultSet res = stat.executeQuery(sql);
                    
                    //penelusuran baris pada tabel
                    while(res.next()){
                    Object[] obj = new Object[4];
                    obj[0] = res.getString("Nama_Petugas");
                    obj[1] = res.getString("ID_Petugas");
                    obj[2] = res.getString("No_Telp");
                    obj[3] = res.getString("Alamat");
                    
                    model.addRow(obj);
                    }
        }catch(SQLException err){
            JOptionPane.showConfirmDialog(null, err.getMessage());
        }
    }
    
