    public void loadData(){
      nama    = txtnama.getText();
      id   = txtid.getText();
      telp   = txttelp.getText();
      alamat = txtalamat.getText();
    }

    public void saveData(){
loadData();
try{
    Statement stat = (Statement) dbkoneksi.getKoneksi().createStatement();
    String sql = "Insert into tblpetugas (Nama_Petugas,ID_Petugas,No_Telp,Alamat)"
            + "values ('"+ nama +"','"+ id +"','"+ telp +"','"+ alamat +"')";
    PreparedStatement p = (PreparedStatement) dbkoneksi.getKoneksi().prepareStatement(sql);
    p.executeUpdate();
    getData();
}catch(SQLException err){
    JOptionPane.showMessageDialog(null, err.getMessage());
}
    }
 public void Reset(){
        nama         ="";
        id      ="";
        telp     ="";
        alamat      ="";
        txtnama.setText(nama);
        txtid.setText(id);
        txttelp.setText(telp);
        txtalamat.setText(alamat);
        
}
    
 public void dataSelect(){
int i = tblpetugas.getSelectedRow();
if (i == -1){
return;
}
txtnama.setText(""+model.getValueAt(i, 0));
txtid.setText(""+model.getValueAt(i, 1));
txttelp.setText(""+model.getValueAt(i, 2));
txtalamat.setText(""+model.getValueAt(i, 3));

}
 public void editData(){
loadData();
try{
    Statement stat = (Statement) dbkoneksi.getKoneksi().createStatement();
    String sql = "UPDATE tblpetugas SET ID_petugas      = '"+ id +"',"
                                + "No_Telp   = '"+ telp +"'," 
                                + "Alamat           = '"+ alamat +"' WHERE Nama_Petugas = '"+ nama +"'";
    PreparedStatement p = (PreparedStatement) dbkoneksi.getKoneksi().prepareStatement(sql);
    p.executeUpdate();
    getData();
    Reset();
    JOptionPane.showMessageDialog(null, "Edit berhasil....");
}catch(SQLException er){
    JOptionPane.showMessageDialog(null, er.getMessage());
}
}
 public void hapusData(){
loadData();
int pesan = JOptionPane.showConfirmDialog(null, "Anda yakin Menghapus data : "+ nama +"?","Konfirmasi",
            JOptionPane.OK_CANCEL_OPTION);
if (pesan == JOptionPane.OK_OPTION){
    try{
        Statement stat = (Statement) dbkoneksi.getKoneksi().createStatement();
        String sql = "DELETE FROM tblpetugas WHERE Nama_Petugas= '"+ nama +"'";
        PreparedStatement p = (PreparedStatement) dbkoneksi.getKoneksi().prepareStatement(sql);
        p.executeUpdate();
        getData();
        Reset();
        JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
    }catch(SQLException er){
        JOptionPane.showMessageDialog(null, er.getMessage());
    }
}
}
 
  

